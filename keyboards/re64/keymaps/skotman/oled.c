static void print_status_narrow(void) {
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_ln_P(PSTR("LAYERBASE"), false);
            break;
        case 3:
            oled_write_ln_P(PSTR("LAYER  FN"), false);
            break;
        case 1:
            oled_write_ln_P(PSTR("LAYER  2"), false);
            break;
        case 2:
            oled_write_ln_P(PSTR("LAYER NUMPAD"), false);
            break;
        default:
            oled_write_P(PSTR("Undef"), false);
    }

		uint8_t led_usb_state = host_keyboard_leds();
	    oled_write_ln_P(PSTR(""), false);
        oled_write_P(IS_LED_ON(led_usb_state, USB_LED_CAPS_LOCK) ? PSTR("CAPS ON ") : PSTR("CAPS OFF"), false);
        oled_write_ln_P(PSTR(""), false);
        oled_write_P(IS_LED_ON(led_usb_state, USB_LED_NUM_LOCK) ? PSTR("NMLCKON ") : PSTR("NMLCKOFF"), false);

}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (is_keyboard_master()) {
        return OLED_ROTATION_270;
    }
	else {
		return OLED_ROTATION_270;
	}
    return rotation;
}

void oled_task_user(void) {
    if(is_oled_on()){
    if (is_keyboard_master()) {
        print_status_narrow();
    } else {
        dva_eve();
    }
    }
}
